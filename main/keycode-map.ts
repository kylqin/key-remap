const mp = mapping.map((m) => {
  return {
    ...m,
    value: m.value.replace('0x', '0x7000000'),
    keys: m.title.replace(/(Keyboard|Keypad) /, "").split(" and "),
    type: /Keyboard/.test(m.title)
      ? 'keyboard'
      : /Keypad/.test(m.title)
        ? 'keypad'
        : 'other'
  };
});

const mpKeyboard = mp.filter(m => m.type === 'keyboard').reduce((acc, m) => {
  for (const key of m.keys) {
    acc.set(key, m)
  }
  return acc
}, new Map())

export function toDetail (key: string) {
  return mpKeyboard.get(key)
}

export function toCode (key: string) {
  return mpKeyboard.get(key).value
}

